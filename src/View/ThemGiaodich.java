    private void btnChonCanhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonCanhoActionPerformed
        // TODO add your handling code here:
        int selectRow = tblSearchCanho.getSelectedRow();
        if (selectRow > -1) {
            Canho canho = dsCanho.get(selectRow);
            if (checkContains(canho)) {
                dsChitiet.add(canho);
                loadDSChitiet(dsChitiet);
            } else {
                showMsg("Căn hộ đã có trong danh sách");
            }
        }
    }//GEN-LAST:event_btnChonCanhoActionPerformed

    private void btnThemGiaodichActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemGiaodichActionPerformed
        // TODO add your handling code here:
        if (khachhang != null) {
            if (dsChitiet.size() > 0) {
                if (indexLoaiGD >= 0) {
                    try {
                        Giaodich giaodich = new Giaodich();
                        giaodich.setKhachhang(khachhang);
                        giaodich.setLoaiGiaodich(indexLoaiGD);
                        giaodich.setNhanvien(qLMain.getNhanvien());
                        giaodich.setSoluong(soluong);
                        giaodich.setTonggia(tonggia);
                        giaodich.setNgaylapGiaodich(new Date(new java.util.Date().getTime()));
                        ArrayList<ChitietGiaodich> dsChitietGiaodichs = new ArrayList<>();
                        giaodich = (Giaodich) giaodichDAO.add(giaodich);
                        for (Canho canho : dsChitiet) {
                            ChitietGiaodich chitietGiaodich = new ChitietGiaodich(giaodich, canho);
                            chitietGiaodichDAO.add(chitietGiaodich);
                            canho.setTrangthai((short) (indexLoaiGD+1));
                            canhoDAO.update(canho);
                        }
                        showMsg("Thêm giao dịch thành công");
                        removeNotify();
                        qLMain.loadTblChitietGD();
                    } catch (Exception ex) {
                        showMsg("Có lỗi xảy ra");
                    }
                } else {
                    showMsg("Chọn loại giao dịch");
                }
            } else {
                showMsg("Chọn ít nhất 1 căn hộ");
            }
        } else {
            showMsg("Thêm thông tin khách hàng");
        }
    }//GEN-LAST:event_btnThemGiaodichActionPerformed

