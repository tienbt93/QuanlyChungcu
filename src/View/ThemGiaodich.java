    private void loadDSChitiet(ArrayList<Canho> dsChitiet) {
        String[] title = {"Mã Căn hộ", "Tên tòa nhà", "Số nhà", "Diện tích", "Số phòng", "Giá bán", "Giá thuê", "Mô tả", "Trạng thái"};
        DefaultTableModel model = new DefaultTableModel(null, title);
        for (int i = 0; i < dsChitiet.size(); i++) {
            model.addRow(dsChitiet.get(i).getObj());
        }
        tblChitietgiaodich.setModel(model);
        updateThongtin(indexLoaiGD);
    }

    private void updateThongtin(int indexGD) {
        tonggia = 0;
        if (indexGD == 0) {
            for (Canho canho : dsChitiet) {
                tonggia += canho.getGiaban();
            }
        } else {
            for (Canho canho : dsChitiet) {
                tonggia += canho.getGiathue();
            }
        }
        soluong=dsChitiet.size();
        lbSoluong.setText(soluong+" Căn hộ");
        lbTongsotien.setText(Common.formatMoney(tonggia));
    }
