    public void loadTblSearchKH(String searchText) {
        dsKH = (ArrayList<Khachhang>) khachhangDAO.getAllByText(searchText);
        String[] title = {"Mã KH", "Tên KH", "CMND", "Ngày sinh"};
        DefaultTableModel model = new DefaultTableModel(null, title);
        for (int i = 0; i < dsKH.size(); i++) {
            model.addRow(dsKH.get(i).getObjSearch());
        }
        tblSearchKH.setModel(model);
    }

    public void loadTblSearchCH(String searchText) {
        dsCanho = (ArrayList<Canho>) canhoDAO.getAllByText(searchText);
        String[] title = {"Mã Căn hộ", "Tòa nhà", "Số nhà", "Giá bán", "Giá thuê"};
        DefaultTableModel model = new DefaultTableModel(null, title);
        for (int i = 0; i < dsCanho.size(); i++) {
            model.addRow(dsCanho.get(i).getObjSearch());
        }
        tblSearchCanho.setModel(model);
    }

    public boolean checkContains(Canho canho) {
        for (Canho ch : dsChitiet) {
            if (ch.getIdCanho() == canho.getIdCanho()) {
                return false;
            }
        }
        return true;
    }

